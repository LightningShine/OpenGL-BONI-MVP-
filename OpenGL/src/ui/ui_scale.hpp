#pragma once

struct GLFWwindow;

// ============================================================================
// Масштаб интерфейса под DPI монитора (модель macOS).
//
// Идея: размер окна решает СКОЛЬКО контента влезает, а DPI монитора решает
// НАСКОЛЬКО КРУПНЫЙ каждый элемент. Весь UI-код задаёт размеры в логических
// пунктах (1 пункт = 1 пиксель при 100% системном масштабе) и переводит их
// в пиксели через points(). При перетаскивании окна на монитор с другим DPI
// GLFW сообщает новый масштаб — шрифты пересобираются, элементы сохраняют
// физический размер, качество текста не падает.
//
// Пределы и размеры шрифтов — в UIConfig (UI_Config.h, секция UI SCALE).
// ============================================================================
namespace ui_scale
{
	/// Читает масштаб монитора, на котором находится окно, и подписывается
	/// на его смену (перетаскивание на другой монитор). Звать один раз,
	/// сразу после создания окна.
	void init(GLFWwindow* window);

	/// Дешёвый периодический пересчёт (раз в ~60 кадров): ловит смену
	/// разрешения и переезд на монитор с той же настройкой Windows, но
	/// другой физической плотностью. Звать раз в кадр из начала кадра.
	void poll(GLFWwindow* window);

	/// Итоговый масштаб UI = масштаб монитора × пользовательский множитель.
	float get();

	/// Переводит логические пункты в пиксели: points(16) при 150% DPI = 24 px.
	float points(float logical_points);

	/// true ровно один раз после смены масштаба. Вызывающий обязан
	/// пересобрать атлас шрифтов и размеры стиля ДО начала кадра ImGui.
	bool consume_scale_changed();

	/// Пользовательский множитель поверх монитора (будущая настройка
	/// "UI Scale 100/125/150%"). 1.0 по умолчанию.
	void set_user_scale(float multiplier);
	float user_scale();
}
